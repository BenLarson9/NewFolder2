
package com.example;
import java.util.Scanner;


public class TrafficLightSwitch {

    public static void main(String args[]) {
Scanner sc = new Scanner(System.in);
     
    System.out.println("Please enter a color code");
    int oyvey = sc.nextInt();
     
    switch (oyvey){
        case 1: System.out.println("The Next color is green.");
            break;
        case 2: System.out.println("The Next color is yellow.");
            break;
        case 3: System.out.println("The Next color is red.");
            break;
        default: System.out.println("That is not a valid color code.");
                  }
        
        
        
    
        
        
        
        
        
        
        
        
        
        
        
    }

}
